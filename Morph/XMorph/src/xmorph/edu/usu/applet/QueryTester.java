    private void submitQuery(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitQuery
        // TODO add your handling code here:
        try {
            // Clear all of the radio buttons
            stronglyTypedButton.setSelected(false);
            wideningTypedButton.setSelected(false);
            narrowingTypedButton.setSelected(false);
            weaklyTypedButton.setSelected(false);
            typeErrorButton.setSelected(false);

            outputBox.setText(morph.execute(queryText.getText()));
            queryShapeText.setText(morph.queryShape.imageXML());
            if (LossInformation.nonAdditive) {
                if (LossInformation.inclusive) {
                    // Is reversible
                    stronglyTypedButton.setSelected(true);
                } else {
                    // Is reversible
                    narrowingTypedButton.setSelected(true);
                }
            } else {
                if (LossInformation.inclusive) {
                    // Is narrowing
                    wideningTypedButton.setSelected(true);
                } else {
                    // Is reversible
                    weaklyTypedButton.setSelected(true);
                }
            }
            typeInfoTextArea.setText(TypeInformation.reportAndReset());
            infoLossTextArea.setText(LossInformation.reportAndReset());
        } catch (XMorphParseException e) {
            outputBox.setText("There is an error in the syntax of the Morph query.\n" + e.msg);
            queryShapeText.setText("Parse error in query, see Output box.");
            typeInfoTextArea.setText("Parse error in query, see Output box.");
            typeErrorButton.setSelected(true);
        } catch (Exception e) {
            StackTraceElement st[] = e.getStackTrace();
            String s = e.toString();
            for (StackTraceElement se : st) {
                s += se.toString();
            }
            outputBox.setText("Error in query, see below.\n" + ErrorInformation.reportAndReset());
            //outputBox.setText(s);
            queryShapeText.setText("Error in query, see Output box.");
            typeInfoTextArea.setText("Error in query, see Output box.\n" + TypeInformation.reportAndReset());
            infoLossTextArea.setText("Error in query, see Output box.\n" + LossInformation.reportAndReset());
            typeErrorButton.setSelected(true);
        }
}//GEN-LAST:event_submitQuery

