    private void parseInput(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_parseInput
        // TODO add your handling code here:
        try {
            stronglyTypedButton.setSelected(false);
            wideningTypedButton.setSelected(false);
            narrowingTypedButton.setSelected(false);
            weaklyTypedButton.setSelected(false);
            typeErrorButton.setSelected(false);

            morph.parse(inputText.getText());
            dataShapeText.setText(morph.dataShape.imageXML());
            outputBox.setText("XML parsed successfully.  Ready to query.");
        } catch (SAXParseException e) {
            String s = "XML Parse error at line " + e.getLineNumber() + " column " + e.getColumnNumber() + ":" + e.getMessage();
            outputBox.setText(s);
            dataShapeText.setText("Error in parsing XML.");
        } catch (Exception e) {
            StackTraceElement st[] = e.getStackTrace();
            String s = e.toString();
            for (StackTraceElement se : st) {
                s += se.toString();
            }
            outputBox.setText(s);
            dataShapeText.setText("Error in parsing XML.");
        }
}//GEN-LAST:event_parseInput

